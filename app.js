function formatNumber(value) {
  return new Intl.NumberFormat('es-CO').format(value || 0);
}

function sumBy(arr, key) {
  return arr.reduce((acc, item) => acc + (Number(item[key]) || 0), 0);
}

function uniqueCount(arr, key) {
  return new Set(arr.map(item => item[key]).filter(Boolean)).size;
}

function buildMunicipioOptions() {
  const municipios = [...new Set(electoralData.map(item => item.municipio))].sort((a,b) => a.localeCompare(b, 'es'));
  municipioFilter.innerHTML = `<option value="TODOS">Todos</option>${municipios.map(m => `<option value="${m}">${m}</option>`).join('')}`;
}

function buildTerritorioOptions(selectedMunicipio = 'TODOS') {
  const territorios = [...new Set(
    electoralData
      .filter(item => selectedMunicipio === 'TODOS' || item.municipio === selectedMunicipio)
      .map(item => item.territorio || 'SIN CLASIFICAR')
  )].sort((a,b) => a.localeCompare(b, 'es'));

  territorioFilter.innerHTML = `<option value="TODOS">Todos</option>${territorios.map(t => `<option value="${t}">${t}</option>`).join('')}`;
}

function initMap() {
  map = L.map('map', { zoomControl: true }).setView([2.5359, -75.5277], 9);

  L.tileLayer('https://{s}.tile.openstreetmap.org/{z}/{x}/{y}.png', {
    maxZoom: 19,
    attribution: '&copy; OpenStreetMap'
  }).addTo(map);

  markersLayer = L.layerGroup().addTo(map);
  renderAll();
}

function getMarkerIcon() {
  return L.divIcon({
    className: 'leaflet-div-icon',
    html: '<div class="custom-marker"></div>',
    iconSize: [18, 18],
    iconAnchor: [9, 9],
    popupAnchor: [0, -8]
  });
}

function renderMarkers(data) {
  markersLayer.clearLayers();
  const bounds = [];

  data.forEach(item => {
    if (!isFinite(item.lat) || !isFinite(item.lng)) return;

    const marker = L.marker([item.lat, item.lng], { icon: getMarkerIcon() });
    marker.bindPopup(`
      <div class="popup-title">${item.puesto}</div>
      <div>${item.municipio} · ${item.territorio}</div>
      <div style="color:#9fb5d8;margin-top:4px">${item.direccion}</div>
      <div class="popup-grid">
        <div class="popup-pill"><span>Mesas</span><strong>${formatNumber(item.mesas)}</strong></div>
        <div class="popup-pill"><span>Total</span><strong>${formatNumber(item.total)}</strong></div>
        <div class="popup-pill"><span>Mujeres</span><strong>${formatNumber(item.mujeres)}</strong></div>
        <div class="popup-pill"><span>Hombres</span><strong>${formatNumber(item.hombres)}</strong></div>
      </div>
    `);
    marker.addTo(markersLayer);
    bounds.push([item.lat, item.lng]);
  });

  if (bounds.length) {
    map.fitBounds(bounds, { padding: [30, 30] });
  }
}

function renderKpis(data) {
  const puestos = data.length;
  const municipios = uniqueCount(data, 'municipio');
  const territorios = uniqueCount(data, 'territorio');
  const mesas = sumBy(data, 'mesas');
  const mujeres = sumBy(data, 'mujeres');
  const hombres = sumBy(data, 'hombres');
  const total = sumBy(data, 'total');
  const brecha = Math.abs(mujeres - hombres);
  const promMesas = puestos ? (mesas / puestos) : 0;
  const promCedulas = puestos ? (total / puestos) : 0;

  const cards = [
    { label: 'Municipios', value: formatNumber(municipios), sub: 'Cobertura del filtro aplicado' },
    { label: 'Territorios', value: formatNumber(territorios), sub: 'Comunas, corregimientos o sin clasificar' },
    { label: 'Puestos de votación', value: formatNumber(puestos), sub: 'Cobertura del filtro aplicado' },
    { label: 'Mesas', value: formatNumber(mesas), sub: `Promedio por puesto: ${promMesas.toFixed(1).replace('.', ',')}` },
    { label: 'Cédulas inscritas', value: formatNumber(total), sub: `Promedio por puesto: ${formatNumber(Math.round(promCedulas))}` },
    { label: 'Brecha absoluta', value: formatNumber(brecha), sub: mujeres >= hombres ? 'Mayor registro femenino' : 'Mayor registro masculino' },
  ];

  kpiGrid.innerHTML = cards.map(card => `
    <article class="card kpi-card">
      <div class="kpi-label">${card.label}</div>
      <div class="kpi-value num">${card.value}</div>
      <div class="kpi-sub">${card.sub}</div>
    </article>
  `).join('');

  recordCountBadge.textContent = `${formatNumber(puestos)} puestos`;
}

function groupByMunicipio(data) {
  const bucket = new Map();
  data.forEach(item => {
    const key = item.municipio || 'SIN CLASIFICAR';
    if (!bucket.has(key)) {
      bucket.set(key, { municipio: key, puestos: 0, mesas: 0, mujeres: 0, hombres: 0, total: 0 });
    }
    const acc = bucket.get(key);
    acc.puestos += 1;
    acc.mesas += item.mesas;
    acc.mujeres += item.mujeres;
    acc.hombres += item.hombres;
    acc.total += item.total;
  });
  return [...bucket.values()].sort((a,b) => b.total - a.total);
}

function renderComunaBars(data) {
  const groups = groupByMunicipio(data);
  const max = Math.max(...groups.map(g => g.total), 1);

  comunaBars.innerHTML = groups.map(item => `
    <div class="bar-item">
      <div class="bar-head">
        <strong>${item.municipio}</strong>
        <span class="num">${formatNumber(item.total)} cédulas</span>
      </div>
      <div class="bar-track"><div class="bar-fill" style="width:${(item.total/max)*100}%"></div></div>
      <div class="bar-meta">
        <span>Puestos: <strong>${formatNumber(item.puestos)}</strong></span>
        <span>Mesas: <strong>${formatNumber(item.mesas)}</strong></span>
        <span>Mujeres: <strong>${formatNumber(item.mujeres)}</strong></span>
        <span>Hombres: <strong>${formatNumber(item.hombres)}</strong></span>
      </div>
    </div>
  `).join('');
}

function renderTopPuestos(data) {
  const top = [...data].sort((a,b) => b.total - a.total).slice(0, 10);
  const max = Math.max(...top.map(g => g.total), 1);

  topPuestos.innerHTML = top.map((item, idx) => `
    <div class="bar-item">
      <div class="bar-head">
        <strong>#${idx + 1} ${item.puesto}</strong>
        <span class="num">${formatNumber(item.total)} inscritos</span>
      </div>
      <div class="bar-track"><div class="bar-fill" style="width:${(item.total/max)*100}%"></div></div>
      <div class="bar-meta">
        <span>${item.municipio}</span>
        <span>${item.territorio}</span>
        <span>Mesas: <strong>${formatNumber(item.mesas)}</strong></span>
      </div>
    </div>
  `).join('');
}

function renderGender(data) {
  const mujeres = sumBy(data, 'mujeres');
  const hombres = sumBy(data, 'hombres');
  const total = mujeres + hombres;
  const mujeresPct = total ? ((mujeres / total) * 100) : 0;
  const hombresPct = total ? ((hombres / total) * 100) : 0;

  genderDonut.style.background = `conic-gradient(var(--pink) 0 ${mujeresPct}%, var(--cyan) ${mujeresPct}% 100%)`;
  donutTotal.textContent = formatNumber(total);

  genderLegend.innerHTML = `
    <div class="legend-item">
      <div class="legend-left">
        <span class="legend-dot" style="background:var(--pink)"></span>
        <div>
          <strong>Mujeres</strong>
          <span>${mujeresPct.toFixed(1).replace('.', ',')}% del total</span>
        </div>
      </div>
      <strong class="num">${formatNumber(mujeres)}</strong>
    </div>
    <div class="legend-item">
      <div class="legend-left">
        <span class="legend-dot" style="background:var(--cyan)"></span>
        <div>
          <strong>Hombres</strong>
          <span>${hombresPct.toFixed(1).replace('.', ',')}% del total</span>
        </div>
      </div>
      <strong class="num">${formatNumber(hombres)}</strong>
    </div>
  `;
}

function renderTable(data) {
  tableMeta.textContent = `Registros visibles: ${formatNumber(data.length)} puestos`;
  tableBody.innerHTML = [...data]
    .sort((a,b) => b.total - a.total)
    .map(item => `
      <tr>
        <td><strong>${item.municipio}</strong></td>
        <td><strong>${item.puesto}</strong></td>
        <td><span class="tag">${item.territorio}</span></td>
        <td>${item.direccion}</td>
        <td class="num">${formatNumber(item.mesas)}</td>
        <td class="num">${formatNumber(item.mujeres)}</td>
        <td class="num">${formatNumber(item.hombres)}</td>
        <td class="num"><strong>${formatNumber(item.total)}</strong></td>
        <td class="num">${item.lat.toFixed(6)}</td>
        <td class="num">${item.lng.toFixed(6)}</td>
      </tr>
    `).join('');
}

function renderInsights(data) {
  if (!data.length) {
    insightsList.innerHTML = '<li>No hay registros para el filtro aplicado.</li>';
    return;
  }

  const municipios = groupByMunicipio(data);
  const topMunicipio = municipios[0];
  const topPuesto = [...data].sort((a,b) => b.total - a.total)[0];
  const topMesas = [...data].sort((a,b) => b.mesas - a.mesas)[0];
  const total = sumBy(data, 'total');

  insightsList.innerHTML = `
    <li>El municipio con más cédulas inscritas es <strong>${topMunicipio.municipio}</strong> con <strong>${formatNumber(topMunicipio.total)}</strong>.</li>
    <li>El puesto con mayor volumen de inscritos es <strong>${topPuesto.puesto}</strong> en <strong>${topPuesto.municipio}</strong> con <strong>${formatNumber(topPuesto.total)}</strong> cédulas.</li>
    <li>El puesto con más mesas es <strong>${topMesas.puesto}</strong> con <strong>${formatNumber(topMesas.mesas)}</strong> mesas.</li>
    <li>El total filtrado representa <strong>${formatNumber(total)}</strong> cédulas distribuidas en <strong>${formatNumber(data.length)}</strong> puestos de votación.</li>
  `;
}

function filterData() {
  const municipio = municipioFilter.value;
  const territorio = territorioFilter.value;
  const term = searchInput.value.trim().toLowerCase();

  currentData = electoralData.filter(item => {
    const matchMunicipio = municipio === 'TODOS' || item.municipio === municipio;
    const matchTerritorio = territorio === 'TODOS' || item.territorio === territorio;
    const searchable = `${item.puesto} ${item.direccion} ${item.municipio} ${item.territorio}`.toLowerCase();
    const matchSearch = !term || searchable.includes(term);
    return matchMunicipio && matchTerritorio && matchSearch;
  });

  renderAll();
}

function renderAll() {
  renderKpis(currentData);
  renderMarkers(currentData);
  renderComunaBars(currentData);
  renderTopPuestos(currentData);
  renderGender(currentData);
  renderTable(currentData);
  renderInsights(currentData);
}

